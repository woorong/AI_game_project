package entities;

public class Entity {
	public float xpos;
	public float ypos;
	public int xsize;
	public int ysize;
	
	public Entity(float initXpos, float initYpos, int initXsize, int initYsize) {
		xpos = initXpos;
		ypos = initYpos;
		xsize = initXsize;
		ysize = initYsize;
	}
	
	
}
